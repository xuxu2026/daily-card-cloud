def _get_fallback_beauty(count: int) -> list:
    """获取本地美文（节日优先）"""
    texts = []

    # 节日优先使用节日美文
    if _is_holiday():
        holiday_texts = BEAUTY_TEXTS.get("holiday", [])
        texts.extend(random.sample(holiday_texts, min(2, len(holiday_texts))))

    # 补充季节美文
    season = _get_season()
    season_texts = BEAUTY_TEXTS.get(season, BEAUTY_TEXTS["default"])
    texts.extend(random.sample(season_texts, min(2, len(season_texts))))

    # 通用美文
    default_texts = BEAUTY_TEXTS.get("default", [])
    texts.extend(random.sample(default_texts, min(1, len(default_texts))))

    # 去重
    unique_texts = list(dict.fromkeys(texts))
    return random.sample(unique_texts, min(count, len(unique_texts)))


def _get_fallback_skincare(count: int) -> list:
    """获取本地护肤贴士（节日优先）"""
    tips = []

    # 节日优先使用节日护肤贴士
    if _is_holiday():
        holiday_tips = SKINCARE_TIPS.get("holiday", [])
        tips.extend(random.sample(holiday_tips, min(2, len(holiday_tips))))

    # 补充季节护肤贴士
    season = _get_season()
